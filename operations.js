// Problem:

/* Make multiple functions that will return the sum, difference, modulus, product, quotient, and exponent respectively.

Please use the following function names:

addition = add

multiply = mul

division = div (both integer & float divisions are accepted)

modulus = mod

exponential = exp

subtraction = sub

Note: All math operations will be: a (operation) b */

// Solution:

function add(a,b){
    return a + b;
};

function div(a,b){
    return a / b;
};

function multiply(a,b){
    return a * b;
};

function mod(a,b){
    return a % b;
}
   
function exponent(a,b){
    return a ** b;
};
    
function subt(a,b){
    return a - b;
}
